#include <stdio.h>
// Inclui a biblioteca padrão de entrada/saída
// Necessária para usar a função printf()

int main() {
    // Ponto de entrada principal do programa
    
    printf("Hello World\n");
    // Imprime "Hello World" seguido de uma quebra de linha (\n)
    
    printf("\t Hellou World");
    // Imprime um tabulação (\t), depois "Hellou World" (sem quebra de linha no final)
    
    return 0;
    // Retorna 0 indicando que o programa terminou com sucesso
}